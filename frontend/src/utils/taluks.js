export const districts = Object.keys(talukData).sort();

export const getTaluksForDistrict = (district) => talukData[district] || [];

/**
 * Fuzzy-match a Google Maps address_components result to find
 * the District and Taluk from our master list.
 * @param {Array} components - Google Maps address_components array
 * @returns {{ district: string|null, taluk: string|null }}
 */
export const detectDistrictAndTaluk = (components) => {
  if (!components || !Array.isArray(components)) return { district: null, taluk: null };

  // Collect all name tokens from address_components
  const allNames = components
    .flatMap(c => [c.long_name, c.short_name])
    .filter(Boolean)
    .map(n => n.toLowerCase().trim());

  let matchedDistrict = null;
  let matchedTaluk = null;

  // Try to find a taluk match first (more specific)
  outer: for (const [district, taluks] of Object.entries(talukData)) {
    for (const taluk of taluks) {
      const talukLower = taluk.toLowerCase();
      if (allNames.some(name => name.includes(talukLower) || talukLower.includes(name))) {
        matchedDistrict = district;
        matchedTaluk = taluk;
        break outer;
      }
    }
  }

  // If no taluk match, try district match
  if (!matchedDistrict) {
    for (const district of Object.keys(talukData)) {
      const distLower = district.toLowerCase();
      if (allNames.some(name => name.includes(distLower) || distLower.includes(name))) {
        matchedDistrict = district;
        break;
      }
    }
  }

  return { district: matchedDistrict, taluk: matchedTaluk };
};
